use serde::{Serialize, Deserialize};

#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct CityPrices {
    pub name: String,
    pub country: String,
    pub currency: String,
    pub currency_symbol: String,
    pub rate_to_rub: f64,
    pub flour: f64,
    pub sugar: f64,
    pub butter: f64,
    pub eggs: f64,
    pub cream: f64,
    pub cheese: f64,
    pub chocolate: f64,
    pub almond_flour: f64,
}

impl CityPrices {
    pub fn in_rub(&self, val: f64) -> f64 { val * self.rate_to_rub }
    pub fn format_price(&self, val: f64) -> String {
        format!("{:.2} {}", val, self.currency_symbol)
    }
}

